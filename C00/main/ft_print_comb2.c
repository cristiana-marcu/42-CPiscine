#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	update_char(int n[])
{
	if (n[3] == 57 && n[2] == 57 && n[1] == 57)
	{
		n[0]++;
		n[1] = 48;
		n[2] = n[0];
		n[3] = n[1];
	}
	if (n[3] == 57 && n[2] == 57)
	{
		n[1]++;
		n[2] = n[0];
		n[3] = n[1];
	}
	if (n[3] == 57)
	{
		n[2]++;
		n[3] = 47;
	}
	n[3]++;
}

void	ft_print_comb2(void)
{
	int n[4];

	n[0] = 48;
	n[1] = 48;
	n[2] = 48;
	n[3] = 49;
	while (n[0] <= 57 && n[1] <= 57 && n[2] <= 57 && n[3] <= 57)
	{
		ft_putchar(n[0]);
		ft_putchar(n[1]);
		ft_putchar(' ');
		ft_putchar(n[2]);
		ft_putchar(n[3]);
		if (!(n[0] == 57 && n[1] == 56 && n[2] == 57 && n[3] == 57))
		{
			ft_putchar(',');
			ft_putchar(' ');
		}
		update_char(n);
	}
}

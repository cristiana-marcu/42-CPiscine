int	ft_iterative_factorial(int nb)
{
	int i;

	i = nb - 1;
	if (nb >= 0)
	{
		while (i >= 1)
		{
			nb = nb * i;
			i--;
		}
	return (nb);
	}
	else
		return (0);
}

int	ft_strcmp(char *s1, char *s2)
{
	int a;

	a = 0;
	while (s1[a] != '\0' && s1[a] == s2[a])
	{
		a++;
	}
	return (int)(unsigned char)(s1[a]) - (int)(unsigned char)(s2[a]);
}
